/** 获取所有 78 张塔罗牌 */
export function getAllCards(): TarotCard[] {
  return [...majorArcana, ...wands, ...cups, ...swords, ...pentacles]
}

/** 获取 22 张大阿卡纳 */
export function getMajorArcana(): TarotCard[] {
  return [...majorArcana]
}

/** 获取 56 张小阿卡纳 */
export function getMinorArcana(): TarotCard[] {
  return [...wands, ...cups, ...swords, ...pentacles]
}

/** 随机抽取指定数量的牌，每张牌随机正/逆位 */
export function drawRandomCards(count: number, deck?: TarotCard[]): DrawnCard[] {
  const source = deck ?? getAllCards()
  // Fisher-Yates 洗牌
  const shuffled = [...source]
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]]
  }
  return shuffled.slice(0, count).map((card) => ({
    card,
    isReversed: Math.random() < 0.5,
  }))
}

// ==================== 类型定义 ====================

export interface TarotCard {
  id: string
  name: string
  nameEn: string
  number: number
  suit: 'major' | 'wands' | 'cups' | 'swords' | 'pentacles'
  emoji: string
  /** Rider-Waite 塔罗牌图片 URL（公共领域） */
  imageUrl: string
  upright: string
  reversed: string
  keywords: string[]
  element: string
  description: string
}

export interface DrawnCard {
  card: TarotCard
  isReversed: boolean
  position?: string
}

export interface TarotReading {
  id: string
  date: string
  mode: 'single' | 'three' | 'celtic'
  cards: DrawnCard[]
}

